package com.xiehongyuan.dao;

import java.util.List;

import org.apache.ibatis.annotations.Param;

import com.xiehongyuan.pojo.Tag;

public interface TagDao {
	/**
	 * @Title: selectById   
	 * @Description: 根据Id，查询对象   
	 * @param: @param id
	 * @param: @return      
	 * @return: Tag      
	 * @throws
	 */
	Tag selectById(@Param("id") Integer id);
	/**
	 * @Title: select   
	 * @Description: 根据Tag查询列表  
	 * @param: @param tag
	 * @param: @return      
	 * @return: List<Tag>      
	 * @throws
	 */
	List<Tag> select(@Param("tag") Tag tag);
	/**
	 * @Title: count   
	 * @Description: 查询数据条数   
	 * @param: @param tag
	 * @param: @return      
	 * @return: int      
	 * @throws
	 */
	int count(@Param("tag") Tag tag);
	/**
	 * @Title: insert   
	 * @Description: 插入一条记录   
	 * @param: @param tag
	 * @param: @return      
	 * @return: int      
	 * @throws
	 */
	int insert(@Param("tag") Tag tag);
	/**
	 * @Title: update   
	 * @Description: 根据Id更新记录 
	 * @param: @param tag
	 * @param: @return      
	 * @return: int      
	 * @throws
	 */
	int update(@Param("tag") Tag tag);
	/**
	 * @Title: deleteById   
	 * @Description: 根据Id删除记录   
	 * @param: @param id
	 * @param: @return      
	 * @return: int      
	 * @throws
	 */
	int deleteById(@Param("id") Integer id);
	/**
	 * @Title: deleteByIds   
	 * @Description: 根据Ids批量删除记录   
	 * @param: @param ids "1,2,3"
	 * @param: @return      
	 * @return: int      
	 * @throws
	 */
	int deleteByIds(@Param("ids") String ids);
}
